import math
from tabulate import tabulate


# Задаём функцию f(x)
# Нужно найти такой x, при котором f(x) = 0
def f(x):
    return 2 ** (-x) - math.sin(x)


# Метод половинного деления
def bisection(a, b, eps):
    # Таблица для хранения всех шагов
    table = []

    # Номер итерации
    k = 0

    # Проверяем, есть ли смена знака на концах отрезка
    # Если смены знака нет, метод половинного деления использовать нельзя
    if f(a) * f(b) > 0:
        print("На этом отрезке нет смены знака.")
        return None

    # Пока длина отрезка больше заданной точности
    while abs(b - a) > eps:
        # Находим середину отрезка
        c = (a + b) / 2

        # Добавляем данные текущего шага в таблицу
        table.append([
            k,      
            a,      
            b,      
            c,      
            f(a),   
            f(b),   
            f(c)    
        ])

        # Если f(a) и f(c) разных знаков,
        # значит корень находится на отрезке [a; c]
        if f(a) * f(c) < 0:
            b = c

        # Иначе корень находится на отрезке [c; b]
        else:
            a = c

        # Увеличиваем номер итерации
        k += 1

    root = (a + b) / 2

    # Возвращаем корень и таблицу
    return root, table


# Начало отрезка для первого корня
a = 0.67

# Конец отрезка для первого корня
b = 0.68

# Точность 10^-3
eps = 10 ** (-3)

# Запускаем метод половинного деления
result = bisection(a, b, eps)

# Если метод сработал
if result is not None:
    # Получаем корень и таблицу
    root, table = result

    # Выводим таблицу
    print("Метод половинного деления:")
    print(tabulate(
        table,
        headers=["k", "a", "b", "c", "f(a)", "f(b)", "f(c)"],
        tablefmt="grid",
        floatfmt=".6f"
    ))

    # Выводим найденный корень
    print(f"\nКорень с точностью 10^-3: x = {root:.6f}")